var rPerc = 1.17;
function r() {
    return Math.pow(rPerc, monthDuration);
}
function credit(sumCred) {
    function monthPay() {
        return sumCred * (0.17 * (r())) / ((r()) - 1);
    }
    function sumOverpay() {
        return monthDuration * monthPay() - sumCred;
    }
    return sumOverpay();
}
